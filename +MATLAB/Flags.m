classdef Flags<uint32
	enumeration
		%% File DesiredAccess
		
		GENERIC_READ                     (0x80000000)
		GENERIC_WRITE                    (0x40000000)
		GENERIC_EXECUTE                  (0x20000000)
		GENERIC_ALL                      (0x10000000)

		%% ShareMode

		FILE_SHARE_READ                 (0x00000001)
		FILE_SHARE_WRITE                (0x00000002)
		FILE_SHARE_DELETE               (0x00000004)

		%% CreationDisposition

		CREATE_NEW          (1)
		CREATE_ALWAYS       (2)
		OPEN_EXISTING       (3)
		OPEN_ALWAYS         (4)
		TRUNCATE_EXISTING   (5)

		%% FlagsAndAttributes

		FILE_ATTRIBUTE_READONLY             (0x00000001)
		FILE_ATTRIBUTE_HIDDEN               (0x00000002)
		FILE_ATTRIBUTE_SYSTEM               (0x00000004)
		FILE_ATTRIBUTE_DIRECTORY            (0x00000010)
		FILE_ATTRIBUTE_ARCHIVE              (0x00000020)
		FILE_ATTRIBUTE_DEVICE               (0x00000040)
		FILE_ATTRIBUTE_NORMAL               (0x00000080)
		FILE_ATTRIBUTE_TEMPORARY            (0x00000100)
		FILE_ATTRIBUTE_SPARSE_FILE          (0x00000200)
		FILE_ATTRIBUTE_REPARSE_POINT        (0x00000400)
		FILE_ATTRIBUTE_COMPRESSED           (0x00000800)
		FILE_ATTRIBUTE_OFFLINE              (0x00001000)
		FILE_ATTRIBUTE_NOT_CONTENT_INDEXED  (0x00002000)
		FILE_ATTRIBUTE_ENCRYPTED            (0x00004000)
		FILE_ATTRIBUTE_INTEGRITY_STREAM     (0x00008000)
		FILE_ATTRIBUTE_VIRTUAL              (0x00010000)
		FILE_ATTRIBUTE_NO_SCRUB_DATA        (0x00020000)
		FILE_ATTRIBUTE_EA                   (0x00040000)
		FILE_ATTRIBUTE_PINNED               (0x00080000)
		FILE_ATTRIBUTE_UNPINNED             (0x00100000)
		FILE_ATTRIBUTE_RECALL_ON_OPEN       (0x00040000)
		FILE_ATTRIBUTE_RECALL_ON_DATA_ACCESS(0x00400000)

		FILE_FLAG_WRITE_THROUGH        (0x80000000)
		FILE_FLAG_OVERLAPPED           (0x40000000)
		FILE_FLAG_NO_BUFFERING         (0x20000000)
		FILE_FLAG_RANDOM_ACCESS        (0x10000000)
		FILE_FLAG_SEQUENTIAL_SCAN      (0x08000000)
		FILE_FLAG_DELETE_ON_CLOSE      (0x04000000)
		FILE_FLAG_BACKUP_SEMANTICS     (0x02000000)
		FILE_FLAG_POSIX_SEMANTICS      (0x01000000)
		FILE_FLAG_SESSION_AWARE        (0x00800000)
		FILE_FLAG_OPEN_REPARSE_POINT   (0x00200000)
		FILE_FLAG_OPEN_NO_RECALL       (0x00100000)
		FILE_FLAG_FIRST_PIPE_INSTANCE  (0x00080000)

		%% MoveMethod

		FILE_BEGIN           (0)
		FILE_CURRENT         (1)
		FILE_END             (2)

		%% Protect

		PAGE_READONLY          (0x02)
		PAGE_READWRITE         (0x04)
		PAGE_WRITECOPY         (0x08)
		PAGE_EXECUTE           (0x10)
		PAGE_EXECUTE_READ      (0x20)
		PAGE_EXECUTE_READWRITE (0x40)
		PAGE_EXECUTE_WRITECOPY (0x80)

		%% MemoryMapping DesiredAccess

		FILE_MAP_WRITE            (0x0002)
		FILE_MAP_READ             (0x0004)
		FILE_MAP_ALL_ACCESS       (983071)

		FILE_MAP_EXECUTE          (0x0020)  % not included in FILE_MAP_ALL_ACCESS

		FILE_MAP_COPY             (0x00000001)

		FILE_MAP_RESERVE          (0x80000000)
		FILE_MAP_TARGETS_INVALID  (0x40000000)
		FILE_MAP_LARGE_PAGES      (0x20000000)

		%% RestartDisposition

		Do_not_restart(0)
		Restart_now(1)
		Inquire(2)
	end
end